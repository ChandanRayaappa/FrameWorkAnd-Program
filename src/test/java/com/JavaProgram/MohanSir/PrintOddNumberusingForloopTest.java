package com.JavaProgram.MohanSir;

public class PrintOddNumberusingForloopTest {
	public static void main(String[] args) {
		
		for(int i=0;i<100;i++) {
			if(i%2==0) {
				System.out.println(i+"number is even number");
			}
			else
			{
				System.out.println(i+"number is odd number");
			}
		}
	}

}
